const policy={};
const seen=new Set();
const SIGNS=[["higher",0],["equal",1],["lower",2]];
function enumerate(rounds){
  const state={rounds};
  const X=arrToMask(myRemaining(state));
  if(X===0)return;
  const {scenarios}=oppBelief(state);
  const belief=beliefMap(scenarios);
  const dedup=X+"|"+bkey(belief);
  if(seen.has(dedup))return; seen.add(dedup);
  for(const colStr of ["black","white"]){
    const col=colStr==="black"?0:1;
    // possible only if some scenario has a card of this colour
    const E=[];for(const[R,w]of belief){const sz=pop(R);for(const o of bits(R)){if((isBlack(o)?0:1)!==col)continue;E.push([o,w/sz]);}}
    if(E.length===0)continue;
    const x=bestCard(X,belief,col);
    policy[stateKey(X,scenarios,colStr)]=x;
    // which signs are reachable for this (col,x)?
    const reach=new Set();
    for(const[o]of E){reach.add(x>o?"higher":x===o?"equal":"lower");}
    for(const[res]of SIGNS){if(reach.has(res))enumerate(rounds.concat([{leader:"opp",myCard:x,oppColor:colStr,result:res}]));}
  }
}
